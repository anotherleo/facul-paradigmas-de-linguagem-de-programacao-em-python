"""
    dicionário em python é um conjunto de pair-values.
"""

pessoas = {101:['Nome John'], 201:['Nome Rachel'], 301:['Nome Max'], 401:['Nome Taylor']}

print(f'{pessoas}')
print(f'primeira pessoa {pessoas[101]}')
print(f'última pessoa {pessoas[401]}')

animais = {1: 'rato', 2: 'gazela', 3: 'elefante'}
print(f'{animais}')
print(f'primeiro animal é {animais[1]}, no meio tem {animais[2]} e depois chegou o {animais[3]}.')
